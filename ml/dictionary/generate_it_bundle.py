def generate_bundle():
    entries = []
    seen = set()

    def add(trigger: str, replacement: str):
        key = (trigger.lower(), replacement)
        if trigger.lower() in _TR_STOPWORDS:
            return  # Türkçe yaygın kelimeyle çakışıyor — atla
        if key not in seen:
            seen.add(key)
            entries.append({"trigger": trigger, "replacement": replacement})

    for tr_pronunciation, correct_term in BASE_TERMS:
        # Temel form
        add(tr_pronunciation, correct_term)

        # Tüm çekim ekleri
        for suffix_tr, suffix_en in SUFFIXES:
            add(tr_pronunciation + suffix_tr, correct_term + suffix_en)

    return entries


def main():
    out_path = Path(__file__).parent / "it_bundle_full.json"
    entries = generate_bundle()
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(entries, f, ensure_ascii=False, indent=2)

    print(f"Oluşturuldu: {out_path}")
    print(f"Toplam giriş: {len(entries)}")

    # Kategori özeti
    print(f"\nBase terim sayısı: {len(BASE_TERMS)}")
    print(f"Suffix sayısı: {len(SUFFIXES)}")
    print(f"Tahmini (base × suffix+1): {len(BASE_TERMS) * (len(SUFFIXES) + 1)}")

    # Önizleme
    print("\nÖrnek (databez):")
    for e in entries:
        if e["trigger"].startswith("databez"):
            print(f"  {e['trigger']} → {e['replacement']}")


if __name__ == "__main__":
    main()
